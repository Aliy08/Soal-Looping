void main() {
  int pertama = 2;
  print("<< Looping Pertama >>");
  while (pertama <= 20) {
    print("$pertama - I Love Coding");
    pertama += 2;
  }

  print("================================");
  
  int kedua = 20;
  print("<<Looping kedua>>");
  while (kedua >=2) {
    print("$kedua - I will become a fullstack developer");
    kedua -= 2;
  }

}